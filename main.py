direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
text = input("Type your message:\n").lower()
shift = int(input("Type the shift number:\n"))

#TODO-1: Combine the encrypt() and decrypt() functions into a single function called caesar(). 

def caesar(plain_text, shift_amount, which_direction):
    cipher_or_plain_text = ""
    for letter in plain_text:
        if letter in ["!", ".", "?", ","]:
            cipher_or_plain_text += letter
            continue
        position = alphabet.index(letter)
        if which_direction == "encode":
            new_position = position + shift_amount
        elif which_direction == "decode":
            new_position = position - shift_amount
        cipher_or_plain_text += alphabet[new_position]
    if which_direction == "encode":
        print(f"The encoded text is {cipher_or_plain_text}")
    elif which_direction == "decode":
        print(f"The decoded text is {cipher_or_plain_text}")

